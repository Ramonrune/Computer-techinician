    public void montaTabela(){
        
        //Cria modelo padr√£o a ser seguido pela tabela
        DefaultTableModel modelo = new DefaultTableModel(){
        public Class getColumnClass(int coluna){
            return coluna==0?Icon.class:Object.class;
        }
            
        };
        ImageIcon verde = new ImageIcon("C:\\Users\\B30\\Documents\\NetBeansProjects\\UsandoJTable\\src\\usandojtable\\verde.png");
        //cabecalhosdascolunas
        
        modelo.setColumnIdentifiers(new String []{"Titulo 1","Titulo 2","Titulo 3"});
        
        tbExemplo.setModel(modelo);
        tbExemplo.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        tbExemplo.getColumnModel().getColumn(0).setPreferredWidth(176);
        tbExemplo.getColumnModel().getColumn(1).setPreferredWidth(176);
        tbExemplo.getColumnModel().getColumn(2).setPreferredWidth(176);
    
        
        for (int i = 0; i < 10; i++) {
            modelo.addRow(new Object[]{verde,"Col1"+i,"Col2"+(i+1),"Col3"+(i+2)});
        }
        
        //grid
        
        tbExemplo.setShowGrid(true);
        //nao permite selecao de mais de um item
        
        tbExemplo.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        //ajusta a altura da  tabela conforme a frame
        tbExemplo.setFillsViewportHeight(true);
        //cor do fundo da tabela
        tbExemplo.setBackground(new Color(0,0,0));
        //cor da letra
        tbExemplo.setForeground(new Color(255,255,255));
        
        //cor do grid
        
        tbExemplo.setGridColor(new Color(255,0,0));
        
        //cor do foco
        
        tbExemplo.setSelectionBackground(new Color(0,255,0));
        
        //cor de fundo
        
        tbExemplo.setSelectionBackground(new Color(100,125,2));
        
        //altura da celula
        
        tbExemplo.setRowHeight(60);
        
        tbExemplo.setFont(new Font("Impact",0,13));
        
        
    }

